    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(textLocal.getText().trim().equals("")||textTitulo.getText().trim().equals("")||textSubtitulo.getText().trim().equals("")||
                textProjetado.getText().trim().equals("")||textDesenhado.getText().trim().equals("")||textRevisado.getText().trim().equals("")||
                textAprovado.getText().trim().equals("")||textCliente.getText().trim().equals("")){
                JOptionPane.showMessageDialog(this, "Um ou mais Campos n√£o foram inseridos!!!");
        }else{
            Cliente c = ClienteDAO.getInstance().getById(Integer.parseInt(textCliente.getText()));
            Projetos p = new Projetos(textLocal.getText().trim(), textTitulo.getText().trim(), textSubtitulo.getText().trim(), textProjetado.getText().trim(), textDesenhado.getText().trim(), 
                    textRevisado.getText().trim(), textAprovado.getText().trim(), c);
            try {
                ProjetosDAO.getInstance().merge(p);
                System.out.println("Os dados foram inseridos");
            } catch (Exception x) {
                System.out.println("Os dados nao foram inseridos");
            }
        }
        List<Projetos> listaProjetos = new ArrayList<>();
        listaProjetos = ProjetosDAO.getInstance().findAll();
        this.popularTabelaProjeto(listaProjetos);
    }//GEN-LAST:event_jButton1ActionPerformed

