    public void popularTabelaFerramentas(List<Ferramenta> plistaferramentas){
        DefaultTableModel tableModel = new DefaultTableModel();
        this.tabelaFerramenta.setModel(tableModel);
        tableModel.addColumn("Id");
        tableModel.addColumn("Codigo");
        tableModel.addColumn("Status");
        tableModel.addColumn("N° nota fiscal");
        tableModel.addColumn("Descrição");
        tableModel.addColumn("Patrimonio");
        tableModel.addColumn("Aquisição");
        tableModel.addColumn("Entrega");
        tableModel.addColumn("Categoria");
        tableModel.addColumn("Local");
        
        
        for(Ferramenta f : plistaferramentas){
            tableModel.addRow(new Object[]{
            f.getId_ferramenta(),
            f.getCodigo_ferra(),
            f.getStatus(),
            f.getNum_nota_fiscal_ferr(),
            f.getDescricao_ferr(),
            f.getPatrimonio_ferr(),
            f.getDt_aquisicao_ferr(),
            f.getDt_entrega_ferr(),
            f.getCategoria_ferr(),
            f.getLocal_ferr()
            }
            );
        }
    }

    public void listarFerramenta() {
        List<Ferramenta> listaFerramenta = new ArrayList();
        listaFerramenta = FerramentaDAO.getInstance().findAll();
        this.popularTabelaFerramentas(listaFerramenta);
    }

